#Estrutura de repetição for - exemplo 2

#sequência
for x in range (10):    
    print(x) 

#exibir determinados valores dentro de um limite máximo e mínimo 
for x in range(3, 9): 
    print(x)

#decrementando de 10 a 1 em -1 
for i in range(10, 0, -1): 
    print(i)
